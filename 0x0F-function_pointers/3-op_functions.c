#include "3-calc.h"

/**
 * op_add - check the code
 * @a: size of triangle
 * @b: size of triangle
 * Return: Always 0.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - check the code
 * @a: size of triangle
 * @b: size of triangle
 * Return: Always 0.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - check the code
 * @a: size of triangle
 * @b: size of triangle
 * Return: Always 0.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - check the code
 * @a: size of triangle
 * @b: size of triangle
 * Return: Always 0.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - check the code
 * @a: size of triangle
 * @b: size of triangle
 * Return: Always 0.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
